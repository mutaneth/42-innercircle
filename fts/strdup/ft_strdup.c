#include <stdlib.h>
#include <stdio.h>

char	*ft_strdup(const char *s1)
{
	char	*s2;
	size_t	i;

	i = 0;
	while (s1[i])
		i++;
	s2 = (char*)malloc(sizeof(char) * (i + 1));
	if (!s2)
		return NULL;
	i = 0;
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}

int		main(int ac, char **ag)
{
	printf("%s\n", ft_strdup(ag[1]));
	return (0);
}
