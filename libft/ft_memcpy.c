#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*d;
	const char	*s;
	size_t		i;

	i = 0;
	d = (char *)dst;
	s = (const char *)src;
	while (i < n && s && d)
	{
		d[i] = s[i];
		i++;
	}
	return ((void *)d);
}
