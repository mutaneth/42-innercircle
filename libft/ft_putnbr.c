#include "libft.h"

void	ft_putnbr(int n)
{
	int	i;
	int	j;

	i = 1;
	if (n < 0)
	{
		ft_putchar('-');
		n = n * (-1);
	}
	j = n;
	while (j > 10)
	{
		j = j / 10;
		i = i * 10;
	}
	while (i > 1)
	{
		ft_putchar((n - n % i) / i + '0');
		n = n % i;
		i = i / 10;
	}
	ft_putchar(n + '0');
}
